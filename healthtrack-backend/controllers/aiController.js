export const generateWeeklyReport = async (req, res) => {
  try {
    const user = await User.findById(req.user._id);
    if (!user) return res.status(404).json({ message: 'User not found' });

    const sevenDaysAgo = new Date();
    sevenDaysAgo.setDate(sevenDaysAgo.getDate() - 7);

    // Fetch entries
    const entries = await Entry.find({
      user: user._id,
      date: { $gte: sevenDaysAgo }
    });

    const statsSummary = {
      workouts: entries.filter(e => e.type === 'workout').length,
      caloriesBurned: entries.filter(e => e.type === 'workout').reduce((sum, e) => sum + (e.caloriesBurned || 0), 0),
      meals: entries.filter(e => e.type === 'meal').length,
      caloriesConsumed: entries.filter(e => e.type === 'meal').reduce((sum, e) => sum + (e.caloriesConsumed || 0), 0),
      weights: entries.filter(e => e.type === 'weight').map(e => e.weightKg)
    };

    const systemPrompt = `You are a senior healthcare analyst on the HealthTrack Pro platform. 
Please write a weekly health progress analysis report in markdown format based on the user's logged activity over the past 7 days:

User Details:
Name: ${user.name}
Age: ${user.age}
Height: ${user.heightCm} cm
Weight: ${user.weightKg} kg

Last 7 Days Logged Stats:
- Workouts completed: ${statsSummary.workouts} (Total calories burned: ${statsSummary.caloriesBurned} kcal)
- Meals tracked: ${statsSummary.meals} (Total calories consumed: ${statsSummary.caloriesConsumed} kcal)
- Logged weight weights list: ${JSON.stringify(statsSummary.weights)}

Please provide:
1. Progress summary & key metrics analysis.
2. Positive highlights (habits done right).
3. Areas of improvement & warnings.
4. Concrete recommendations for the next 7 days.
Keep it encouraging, and end with the standard disclaimer that this report is AI-generated for general wellness purposes and should not be used as clinical diagnosis.`;

    const report = await callGemini(systemPrompt, false);
    res.json({ report });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};
