// General helper to call Gemini
const callGemini = async (prompt, isJson = false) => {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey || apiKey === 'YOUR_GEMINI_API_KEY') {
    console.warn('GEMINI_API_KEY is not defined. Falling back to mock AI responses.');
    return getFallbackResponse(prompt, isJson);
  }

  try {
    const payload = {
      contents: [{ parts: [{ text: prompt }] }]
    };
    if (isJson) {
      payload.generationConfig = {
        responseMimeType: "application/json"
      };
    }

    const response = await axios.post(
      `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${apiKey}`,
      payload,
      { headers: { 'Content-Type': 'application/json' } }
    );

    const resultText = response.data?.candidates?.[0]?.content?.parts?.[0]?.text;
    if (!resultText) throw new Error('Empty response from Gemini API');
    return isJson ? JSON.parse(resultText) : resultText;
  } catch (error) {
    console.error('Gemini API Error:', error.message);
    return getFallbackResponse(prompt, isJson);
  }
};

