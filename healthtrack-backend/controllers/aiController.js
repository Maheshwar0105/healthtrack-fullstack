// Endpoints
export const chatAssistant = async (req, res) => {
  try {
    const { message, history } = req.body;
    if (!message) return res.status(400).json({ message: 'Message is required' });

    const systemPrompt = `You are a professional, empathetic, and knowledgeable AI Health Assistant on the HealthTrack platform. 
Your goal is to guide users towards healthier choices by answering wellness questions, suggesting habits, and offering fitness/diet tips.
Always maintain a helpful and professional tone.
CRITICAL: Include a brief medical disclaimer at the bottom of your response stating that you are an AI assistant and not a medical doctor, and users should consult a physician for urgent health questions.

User history context (if any): ${JSON.stringify(history || [])}
User query: ${message}
Response:`;

    const reply = await callGemini(systemPrompt, false);
    res.json({ reply });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

